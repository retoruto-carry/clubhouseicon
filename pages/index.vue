<template>
  <div class="container px-8 py-8 mx-auto">
    <div>
      <header>
        <h1 class="text-xl md:text-2xl">
          <span class="mr-2">👋 </span>Clubhouse アイコンメーカー
        </h1>
      </header>
      <main class="mt-4">
        <section>
          <label class="cursor-pointer hover:bg-blue">
            <SVGElement
              v-show="!url"
              name="selectPhoto"
              class="mx-auto iconFileInput"
            />
            <Icon
              v-show="url"
              ref="icon"
              :url="url"
              :text="text"
              :border-color="borderColor"
              :border-width="shouldShowBorder ? borderWidth : 0"
              :width="150"
              :icon-style="iconStyle"
              class="mx-auto"
            />
            <input
              class="hidden"
              type="file"
              accept="image/jpg,image/png,image/gif"
              @change="handleInputImage"
            />
          </label>
        </section>
        <section class="mt-8">
          <h2 class="heading"><span class="mr-2">✏</span> テキスト</h2>
          <input
            v-model="text"
            class="w-full h-10 px-3 mt-2 text-base text-gray-700 placeholder-gray-600 border rounded-lg focus:shadow-outline"
            type="text"
            placeholder="聞き専"
            @input="showStyle"
          />
          <div class="mt-2 overflow-x-scroll whitespace-no-wrap">
            <AppSubButton
              v-for="suggestionText in suggestionTexts"
              :key="suggestionText"
              class="mr-2"
              :label="suggestionText"
              @click="handleUpdateText(suggestionText)"
            />
          </div>
        </section>
        <transition name="fade">
          <section v-if="shouldShowStyle" class="mt-8">
            <h2 class="heading">
              <span class="mr-2">🎨</span> スタイル
              <small><small class="ml-2">（追加中）</small></small>
            </h2>
            <IconStyleSettingForm
              v-model="iconStyle"
              class="mt-2"
              :text="text"
              :url="url"
              :border-color="borderColor"
              :border-width="shouldShowBorder ? borderWidth : 0"
            />
          </section>
        </transition>
        <section class="mt-8">
          <h2 class="inline heading"><span class="mr-2">🖼</span> ボーダー</h2>
          <AppSwitch v-model="shouldShowBorder" class="inline ml-2" />
          <template v-if="shouldShowBorder">
            <input v-model="borderColor" type="color" class="block" />
            <input
              v-model="borderWidth"
              type="range"
              min="5"
              max="12"
              class="block"
            />
          </template>
        </section>
        <section class="mt-8 text-center">
          <AppButton
            :is-loading="isLoading"
            :label="'保存'"
            @click="handleSaveImage"
          >
            <template v-slot:icon><span>💾 </span></template>
          </AppButton>
        </section>
      </main>
      <AppModal
        v-show="shouldShowResultModal"
        class="text-center"
        @close="handleResultModalClose"
      >
        <p class="text-lg font-semibold">
          <span class="mr-2">🎉</span>完成しました<span class="ml-2">🎉</span>
        </p>
        <Icon
          v-show="url"
          ref="icon"
          :url="url"
          :text="text"
          :border-color="borderColor"
          :border-width="shouldShowBorder ? borderWidth : 0"
          :width="170"
          :icon-style="iconStyle"
          class="mx-auto"
        />
        <p class="text-sm">Clubhouseのアイコンに設定しよう</p>
        <p class="mt-8 text-sm">＼ みんなにこのサービスを教えてね🙏 ／</p>
        <TwitterButton class="mt-2" />
      </AppModal>
    </div>
  </div>
</template>

<script lang="ts">
import Vue from 'vue'
import { IconStyle } from '~/types/icon'

type HTMLInputEvent = Event & {
  target: HTMLInputElement & EventTarget
}
type LocalData = {
  url: string
  text: string
  borderColor: string
  borderWidth: number
  isLoading: boolean
  shouldShowBorder: boolean
  shouldShowStyle: boolean
  iconStyle: IconStyle
  shouldShowResultModal: boolean
}

const suggestionTexts = [
  '聞き専',
  '離席中',
  '移動中',
  '作業中',
  '仕事中',
  '話しかけて',
  'お風呂中',
]

export default Vue.extend({
  data(): LocalData {
    return {
      url: '',
      text: '',
      borderColor: '#fafad2',
      borderWidth: 8,
      isLoading: false,
      shouldShowBorder: true,
      shouldShowStyle: false,
      iconStyle: {
        name: 'None',
        option: null,
      },
      shouldShowResultModal: false,
    }
  },
  computed: {
    suggestionTexts(): string[] {
      return suggestionTexts
    },
  },
  methods: {
    handleInputImage(e: HTMLInputEvent) {
      if (!e.target.files?.length) {
        return
      }
      const file = e.target.files[0]
      this.url = window.URL.createObjectURL(file)
    },
    async handleSaveImage() {
      this.isLoading = true
      const icon = this.$refs.icon as any
      await icon.downloadImage()
      this.isLoading = false
      this.shouldShowResultModal = true
    },
    showStyle() {
      if (this.shouldShowStyle) return
      this.shouldShowStyle = true
      this.iconStyle = {
        name: 'BlackFilterWhiteText',
        option: {
          fontColor: '#aaa',
        },
      }
    },
    handleResultModalClose() {
      this.shouldShowResultModal = false
    },
    handleUpdateText(text: string) {
      this.text = text
      this.showStyle()
    },
  },
})
</script>

